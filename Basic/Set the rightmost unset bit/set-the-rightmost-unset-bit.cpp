class Solution
{
public:
    int setBit(int N)
    {
        // Write Your Code here
        int k=0;
        while(k<32)
        {
            if(!(N&(1<<k))) 
            {
            N=N|(1<<k);
            break;
            }
            k++;
        }
        return N;
    }
};

